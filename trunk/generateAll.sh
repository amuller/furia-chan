# Example on how to use BytecodeFrag to generate fragments:

java -Xmx4000M org.kit.furia.BytecodeFrag -dm -input ~/ALMACEN/SCAM/EverythingClass/ -output ~/ALMACEN/BigDB/Base

java -Xmx4000M org.kit.furia.BytecodeFrag -dm -input ~/ALMACEN/SCAM/JPackageClassObfuscatedZelix -output ~/ALMACEN/BigDB/Zelix

java -Xmx4000M org.kit.furia.BytecodeFrag -dm -input ~/ALMACEN/SCAM/JPackageClassObfuscatedSandMarkNoClassEnc -output ~/ALMACEN/BigDB/SandMark